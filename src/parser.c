static bool ts_lex(TSLexer *lexer, TSStateId state) {
  START_LEXER();
  switch (state) {
    case 0:
      if (eof) ADVANCE(11);
      if (lookahead == '\t') ADVANCE(21);
      if (lookahead == '\n') ADVANCE(22);
      if (lookahead == ' ') ADVANCE(20);
      if (lookahead == '-') ADVANCE(16);
      if (lookahead == ':') ADVANCE(14);
      if (lookahead == '<') ADVANCE(18);
      if (lookahead == '[') ADVANCE(25);
      if (lookahead == ']') ADVANCE(26);
      if (lookahead == 'X' ||
          lookahead == 'x') ADVANCE(23);
      if (lookahead != 0) ADVANCE(23);
      END_STATE();
    case 1:
      if (lookahead == '\n') ADVANCE(22);
      if (lookahead == ' ') ADVANCE(1);
      END_STATE();
    case 2:
      if (lookahead == '\n') ADVANCE(22);
      if (lookahead == ' ') ADVANCE(1);
      if (lookahead == '-') ADVANCE(4);
      if (lookahead == ':') ADVANCE(14);
      if (lookahead == ']') ADVANCE(26);
      END_STATE();
    case 3:
      if (lookahead == ' ') ADVANCE(24);
      END_STATE();
    case 4:
      if (lookahead == '-') ADVANCE(13);
      END_STATE();
    case 5:
      if (lookahead == ']') ADVANCE(26);
      if (lookahead == 'X' ||
          lookahead == 'x') ADVANCE(27);
      if (lookahead != 0) ADVANCE(28);
      END_STATE();
    case 6:
      if (lookahead != 0 &&
          lookahead != '\t' &&
          lookahead != '\n' &&
          lookahead != ' ') ADVANCE(19);
      END_STATE();
    case 7:
      if (lookahead != 0 &&
          lookahead != '\n' &&
          lookahead != ':') ADVANCE(12);
      END_STATE();
    case 8:
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(15);
      END_STATE();
    case 9:
      if (eof) ADVANCE(11);
      if (lookahead == '\n') ADVANCE(22);
      if (lookahead == ' ') ADVANCE(1);
      if (lookahead == '-') ADVANCE(17);
      if (lookahead == '<') ADVANCE(18);
      if (lookahead != 0 &&
          lookahead != '\t') ADVANCE(23);
      END_STATE();
    case 10:
      if (eof) ADVANCE(11);
      if (lookahead == '-') ADVANCE(17);
      if (lookahead == '<') ADVANCE(18);
      if (lookahead == '[') ADVANCE(25);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(21);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(23);
      END_STATE();
    case 11:
      ACCEPT_TOKEN(ts_builtin_sym_end);
      END_STATE();
    case 12:
      ACCEPT_TOKEN(aux_sym_attr_bool_value_token1);
      if (lookahead != 0 &&
          lookahead != '\n' &&
          lookahead != ':') ADVANCE(12);
      END_STATE();
    case 13:
      ACCEPT_TOKEN(sym_attr_dashes);
      END_STATE();
    case 14:
      ACCEPT_TOKEN(sym_attr_separator);
      END_STATE();
    case 15:
      ACCEPT_TOKEN(sym_attr_value);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(15);
      END_STATE();
    case 16:
      ACCEPT_TOKEN(sym_list_dash);
      if (lookahead == '-') ADVANCE(13);
      END_STATE();
    case 17:
      ACCEPT_TOKEN(sym_list_dash);
      if (lookahead == '-') ADVANCE(3);
      END_STATE();
    case 18:
      ACCEPT_TOKEN(anon_sym_LT);
      END_STATE();
    case 19:
      ACCEPT_TOKEN(sym_following_word_chars);
      if (lookahead != 0 &&
          lookahead != '\t' &&
          lookahead != '\n' &&
          lookahead != ' ') ADVANCE(19);
      END_STATE();
    case 20:
      ACCEPT_TOKEN(sym_nb_whitespace);
      if (lookahead == '\t') ADVANCE(21);
      if (lookahead == '\n') ADVANCE(22);
      if (lookahead == ' ') ADVANCE(20);
      END_STATE();
    case 21:
      ACCEPT_TOKEN(sym_nb_whitespace);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(21);
      END_STATE();
    case 22:
      ACCEPT_TOKEN(sym_newline);
      END_STATE();
    case 23:
      ACCEPT_TOKEN(sym_non_lt_char);
      END_STATE();
    case 24:
      ACCEPT_TOKEN(sym_section_dashes);
      if (lookahead == ' ') ADVANCE(24);
      END_STATE();
    case 25:
      ACCEPT_TOKEN(sym_todo_left_bracket);
      END_STATE();
    case 26:
      ACCEPT_TOKEN(sym_todo_right_bracket);
      END_STATE();
    case 27:
      ACCEPT_TOKEN(sym_todo_checkmark);
      END_STATE();
    case 28:
      ACCEPT_TOKEN(sym_not_a_checkmark);
      if (lookahead != 0 &&
          lookahead != 'X' &&
          lookahead != ']' &&
          lookahead != 'x') ADVANCE(28);
      END_STATE();
    default:
      return false;
  }
}

