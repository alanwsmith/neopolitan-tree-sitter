      if (lookahead == ':') ADVANCE(14);
      if (lookahead == '<') ADVANCE(18);
      if (lookahead == '[') ADVANCE(25);
      if (lookahead == ']') ADVANCE(26);
      if (lookahead == 'X' ||
          lookahead == 'x') ADVANCE(23);
      if (lookahead != 0) ADVANCE(23);
      END_STATE();
    case 1:
